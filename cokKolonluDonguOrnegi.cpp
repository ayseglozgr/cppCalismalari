#include<iostream>
using namespace std;

int main()
{
	for (int a = 1; a < 6; a++)
	{
		for (int k = 1; k < 6; k++)
		{
			if (a == 1)
			{
				cout << a *	k << " ";
			}
			else if (a == 2)
			{
				cout << a * k << " ";
			}
			else if (a == 3)
			{
				cout << a * k << " ";
			}
			else if (a == 4)
			{
				cout << a * k << " ";
			}
			else
			{
				cout << a * k << " ";
			}
		}
		
		cout << endl;
	}
}